	$('#search-btn').on("click", function() {
		$("#search-section").toggle('slow', 'linear', function() {
			if ($(this).css('display') == 'block') {
				$(".carousel-caption").css('right', '30%');
			} else {
				$(".carousel-caption").css('right', '0');
			}
		});
	});
});
</script>
