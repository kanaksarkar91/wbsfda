				<h5 class="bottom_line">Characters of cultivable fish </h5>

				<div class="card my-4 card_border">
					<div class="card-body">
						<p>The following criteria should be considered before selecting a fish for farming purpose.</p>
						<ul class="check_list list-style-none my-4">
							<li><i class="las la-check text-info"></i> <b>Rate of growth:</b> Fish which grow to a larger size in shorter period are suitable for culture. Examples Carps.
							</li>
							<li><i class="las la-check text-info"></i> <b>Adaptation to climate:</b> The cultured species of fish should be able to adapt to the local climatic conditions of the farm.
							</li>
							<li><i class="las la-check text-info"></i> <b>Tolerance:</b> The fish should have the capacity to tolerate wide fluctuations in the physico- chemical conditions such as oxygen, salinity, temperature of the water.
							</li>
							<li><i class="las la-check text-info"></i> <b>Acceptance of artificial feed:</b> When more number of fish is to be accommodated in a limited space, there is the need for supplementary feeding on compounded diets. The fish should
								show ready preference for these feeds.
							</li>
							<li><i class="las la-check text-info"></i> <b>Resistance:</b> It is desirable that the cultured fish is hardly enough to resist the common diseases and attack of parasites.
							</li>
							<li><i class="las la-check text-info"></i> <b>Amiability and compatibility:</b> The fishes proposed to be cultured together ('poly culture') should be able to live together without interfering or attacking the other.
							</li>
							<li><i class="las la-check text-info"></i> <b>Conversion efficiency:</b> The species of fish which give more edible flesh per unit of food consumed is preferred.
							</li>
							<li><i class="las la-check text-info"></i> <b>Consumer's preference:</b> Food preference of people varies with the geographic regions. Hence, the species cultured should be easily marketable locally or to the targeted consumers.
							</li>

						</ul>

