				<p class="mb-4">Pisciculture or fish culture, included under the broad term 'Aquaculture', can be defined as the 'farming and husbandry of economically important fish, under controlled conditions. Pisciculture plays an important role in the economy
					of West Bengal. It helps in augmenting food supply, generating employment, raising nutritional level and earning revenue.

